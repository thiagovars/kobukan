              <h3><strong>Giri:</strong> A arte da gratidão</h3>
              <p class="tall" style="text-align: right;">por Angélica Djenane Philippe</p>
              <p class="tall" style="text-align: right;">Sensei Eduardo Mendes Ribeiro Correa1</p>

              <div style="float: right; width: 100%;">
                <p class="tall" style="text-align: justify; width: 430px; font-style: italic; float: right;">“A gratidão é uma oração sincera, um sentimento profundo de
                reverência pelo caminho e pelo modo de vida do Aikidô. Quando a 
                ferramenta se desgastava ela não era simplesmente jogada fora, mas 
                formalmente recolhida numa cerimônia especial
                longo e leal serviço.” (Morihei Ueshiba)</p>
              </div>
              <p class="tall">A pós-modernidade teve, como uma de suas características marcantes, na esfera
              comportamental, o estímulo à competitividade e ao individualismo. Os resultados não nos 
              fizeram necessariamente uma sociedade mais feliz e realizada. Em contraposição, novos 
              paradigmas nos convidam a uma revisão de valores e nos levam a perceber que estamos 
              inseridos em uma teia de relações, onde cada ação pontual pode gerar impactos em uma escala 
              maior, cujos limites não podemos precisar.</p>
                          <p class="tall">Com raízes em teorias científicas da química, física ou biologia, novas referências
              contribuem para a ruptura do paradigma reducionista, construindo as bases para a compreensão 
              da complexidade dos fenômenos. Passamos assim a compreender a “teia” que interconecta 
              todos os seres e os elementos do planeta, verificando que pequenos fatos, aparentemente 
              irrelevantes, podem ter resultados de significativas conseqüências.</p>
                          <p class="tall">Vivemos em um mundo de relações, onde a forma como interagimos com nossos pares
              passa a ser uma variável significativa para o sucesso ou insucesso das “organizações”, sejam 
              elas associações, família, empresas. A partir do momento em que nos percebemos como parte 
              de um todo, verificamos que a forma como consolidamos nossas relações é substancialmente 
              importante para o êxito de nossos projetos e empreendimentos. Neste sentido, acreditamos que 
              um valor é fundamentalmente importante: o respeito, sobretudo aquele galgado na gratidão.</p>
                          <p class="tall">Neste sentido destacamos um termo de origem nipônica que, na nossa compreensão,
              representa eficazmente este conceito: “GIRI”
              equivocadamente apropriado para promover violências e autoritarismos3
              ao seu sentido mais nobre, onde é compreendido como “gratidão”, ou “respeito”, ou, melhor 
              ainda, “o respeito galgado na gratidão”.</p>
                          <p class="tall">Falamos aqui na consideração e sentimento de gratidão que devemos ter por todos
              aqueles que fazem algo em nosso benefício, muitas vezes algo que nós mesmos não poderíamos 
              fazer: da pessoa que zelosamente prepara o café, a que mantém limpos nossos cômodos, aos 
              nossos chefes e líderes que nos mostraram caminhos, assumiram responsabilidades.</p>
                          <p class="tall">Acreditamos que é importante dedicarmos o “GIRI”, sobretudo, àqueles que nos
              ensinaram algo, muitas vezes colocando ao nosso dispor, de forma suave, conhecimentos 
              que exigiram de si dedicação e sacrifico para serem obtidos. Quem nos doa um de seus bens 
              mais preciosos: o tempo, a dedicação e o saber, nos conduzem a um infinito compromisso de 
              gratidão.</p>
                          <p class="tall">Não são raras as vezes que os vícios humanos do egoísmo e do orgulho nos ofuscam
              a percepção do quanto devemos aos que nos cercam, e, sobretudo, aos nossos orientadores, 
              obstaculizando, pela exacerbação do individualismo, nossa compreensão do “prazer de servir”. 
              Muitas vezes, a obediência e o respeito são percebidos de forma depreciativa, traduzidos como 
              subserviência e como uma fraqueza de caráter em nossa sociedade competitiva.</p>
                          <p class="tall">Contrariamente, os samurais, a classe guerreira do Japão feudal, tinham este conceito
              como base de suas atividades. Em seu sentido etimológico, a própria expressão relaciona-se ao 
              ato de servir, e ainda assim os samurais são associados à imagem de coragem e autoridade.</p>
                          <p class="tall">Pensamos que servir com amor e respeito é uma forma de honrar àqueles a quem somos
              gratos, estabelecendo relações harmoniosas com todas as pessoas. Ao contrário de representar 
              sacrifício e exploração, o bem servir nos conduz ao sentimento de realização, de cumprimento 
              de nossa missão.</p>
                          <p class="tall">Nosso sensei nos ensina que devemos lembrar com carinho e gratidão do legado de
              nosso shihan, tendo por ele o mesmo sentimento que temos por um ancestral. Se nossos mestres 
              são como pais no AIKIDÔ, os mestres de nossos mestres são nossos avós. Este respeito é 
              demonstrado pela devoção ao AIKIDÔ, pelo amor na sua prática, e pela busca de União entre 
              todos os federados.</p>
                          <p class="tall">Se nossas relações, sejam elas hierárquicas ou horizontais, forem galgadas sobre os
              conceitos de “GIRI” teremos dojos mais saudáveis, sustentados pelo respeito e gratidão mútuos, 
              permitindo que o sentimento de parceria seja potencializado, e, por decorrência, obteremos a 
              formação de equipes com melhores desempenhos, mais realizadas em suas atividades e com a 
              harmonia que tanto buscamos desenvolver em nossa Arte.</p>
