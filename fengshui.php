							<h3><strong>Feng Shui:</strong> O trato do visível e do invisível</h3>
							<p class="tall">Ao longo dos séculos, os sábios chineses desenvolveram elaborados métodos e sistemas matemáticos estruturados em torno da filosofia taoísta para mapear as características magnéticas de uma edificação, mesmo que ela ainda não tenha sido construída. O Feng Shui trabalha cada ambiente em dois diferentes níveis: o visível e o invisível.
							</p>
							<p class="tall">
							<strong>O aspecto visível</strong> se refere a tudo que podemos ver, as diversas formas que estruturam cada espaço e as relações aparentes entre elas. Sua observação poderia indicar o que está errado num determinado ambiente, por exemplo, os ensinamentos do feng shui relatam que seria nocivo: a porta principal alinhada com a porta dos fundos; a escada alinhada à porta de entrada; ou objetos pontiagudos ou de aparência desagradável na direção de portas ou janelas. Estas características são relativamente fáceis de remediar, segundo os consultores, com freqüência o tratamento conduz a resultados efetivos.</p>

							<p class="tall">
							<strong>Os aspectos invisíveis</strong> são considerados pelos praticantes desta arte até mesmo mais importantes que os aspectos visíveis. Somente os métodos mais elaborados do Feng Shui são capazes de detectar as "influências invisíveis" de uma edificação. Estas características supostamente explicam porque intuitivamente sentimos alguns ambientes ou locais como "ruins" e outros como "bons".</p>

							<p class="tall">
							Como o invisível supostamente não poderia ser percebido diretamente pelos sentidos, seu estudo é realizado através de cálculos matemáticos que descrevem o campo eletromagnético existente num determinado espaço, situando-o em relação à planta do local ou edificação que está sendo trabalhado.</p>

							<p class="tall">
							A base para o entendimento destes aspectos invisíveis seria a compreensão de que o alinhamento (orientação do imóvel em relação aos campos eletromagnéticos) e as características do momento em que foi construído (relacionadas também aos aspectos da vida estudados pela Astrologia Chinesa) contribuem para que o mesmo manifeste ou atraia certos tipos de vibrações.</p>

							<p class="tall">
							De forma prática e objetiva, o consultor usa uma bússola para descobrir a orientação desses campos e fazer o estudo das características eletromagnéticas do local ou ambiente, registrando os aspectos percebidos, benéficos ou não.</p>

							<p class="tall">
							Segundo os praticantes de Feng Shui, não é possível corrigir problemas visíveis sem que também sejam determinados, ou "mapeados", estes aspectos invisíveis. Sem isso os resultados não serão duradouros, não importa o que tenha sido feito no nível do visível. As influências nocivas invisíveis precisam também ser corrigidas no nível visível – trabalhando a cor, a forma e os materiais associados aos diversos aspectos do espaço em estudo.</p>
