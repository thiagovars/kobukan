							<h3><strong>Feng Shui:</strong> A Origem</h3>
							<p class="drop-caps">A origem da expressão "Feng Shui" está no Zang Shu (O Livro dos Enterros) escrito pelo Mestre Guo Pu (276-324 d.C). O termo é citado na seguinte sentença:
							</p>
							<p class="tall">
							O Qi é disperso pelo vento (feng) e acolhido pela água (shui). green bell
							O Feng Shui é uma corrente de pensamento analítico com tradição de mais de 4000 anos. Os mestres chineses que o estruturaram teriam percebido que cada área natural, terreno ou edificação seria dotada de sua própria vibração influenciada pela presença do Ch'i (chamada em chinês de qi), e estaria sujeita às várias influências do ambiente que a circunda.</p>

							<p class="tall">
							Constatando que certos tipos de vibrações presentes no ambiente e em seu entorno poderiam agir de modo benéfico para o corpo e a mente, enquanto que outros tipos tenderiam a ser prejudiciais, supostamente compreenderam a importância de estudar como situar as edificações, móveis e objetos da maneira mais adequada para favorecer seus usuários, segundo esta interpretação da natureza.</p>

							<p class="tall">
							Segundo as ideias pregadas pelo Feng Shui, quando as pessoas buscam este equilíbrio com as forças benéficas da Natureza, podem gozar de saúde, boa sorte e prosperidade. Quando as ignoram e se alinham com influências nocivas, podem experimentar dificuldades e obstáculos que podem se expressar como doenças, má sorte ou indisposição. Claro está que tais sentenças fazem parte desta crença e não são de forma alguma endossadas pela ciência.</p>

							<p class="tall">
							Os mestres taoístas que desenvolveram esta arte, não utilizavam-na isoladamente: consideravam-na mais um instrumento de equilíbrio a ser utilizado em conjunto com outras práticas articuladas à Medicina tradicional chinesa, como a acupuntura, a meditação, e o Tai Chi Chuan.</p>
