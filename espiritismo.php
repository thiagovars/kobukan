    <h3><strong>Aikido e o Espiritismo:</strong> Qual a relação?</h3>
    <p class="tall" style="text-align: right;">por Fabrício Benedet</p>

    <div style="float: right; width: 100%;">
      <p class="tall" style="text-align: justify; width: 430px; font-style: italic; float: right;">O Presente artigo tem como escopo
      traçar um paralelo de relação existente entre a arte marcial do aikido, criada no Japão por Morihei Ueshiba e o 
      Espiritismo, como pré-requisito à obtenção do título de "Nidan" (segundo grau da faixa preta de Aikido).</p>

    </div>
      <p class="tall">Antes de adentrarmos ao mérito do presente artigo, é crucial que tenhamos bem definidos os conceitos
    principais inerentes ao tema que será abordado.</p>

      <p class="tall">Resumidamente, Aikido é uma arte marcial japonesa desenvolvida pelo mestre Morihei Ueshiba (1883-1969) 
    entre os anos de 1930 e 1960, como um compêndio dos seus estudos marciais, filosofia e crenças religiosas. O Aikido é 
    frequentemente traduzido como “o caminho da unificação (com) da energia da vida”<sup>1</sup> ou "o caminho do espírito harmonioso"<sup>2</sup></p>

      <p class="tall">O objetivo de Ueshiba foi criar uma arte em que os seus praticantes pudessem defender-se a si próprios a 
    partir do ataque do adversário, nunca o iniciando.</p>

      <p class="tall">Executadas sempre através da uma reação a um ataque anterior, as técnicas de Aikido buscam sempre 
    redirecionar a força adversária, ao invés de combatê-la diretamente.</p>

      <p class="tall">Ueshiba concebeu o Aikido a partir da suas experiências com dezenas de artes marciais, mas principalmente 
    baseando-se no estilo daito-ryu aiki-jujutsu, do qual um dos principais expoentes foi Sokaku Takeda, ao qual incorporou 
    técnicas do kenjutsu (técnicas da espada) e do jojutsu (técnicas do bastão curto).</p>

      <p class="tall">Um importante atributo do Aikido é seu apego ao desenvolvimento espiritual.</p>

      <p class="tall">Na segunda metade da década de 1920, retornando à terra natal, Ueshiba conheceu e foi profundamente 
    influenciado por Onisaburo Deguchi, líder espiritual da religião Oomoto (uma espécie de movimento neo-xintoísta) em Ayabe<sup>3</sup>.</p>

      <p class="tall">Uma das características principais da Oomoto baseia-se na “consecução da utopia durante uma vida”. 
    Esta foi uma influência de significante importância sobre a filosofia das artes marciais de Ueshiba, de amor e compaixão 
    principalmente para “aqueles cujo ódio é o seu principal infortúnio”, de forma que o Aikido evidencia 
    essa filosofia numa essência apurada através de uma espécie de masterização das artes marciais, para que seja possível 
    receber um ataque e redirecioná-lo inofensivamente em sentido oposto.</p>

      <p class="tall">Esse aspecto religioso, combinado com treino e com algumas correntes ideológicas e políticas, criou o 
    alicerce do qual emergiu o Aikido. Pode-se afirmar que o Aikido surgiu de um processo de evolução pessoal, o qual ficou
    marcado como paradigma a ser seguido pelos Aikidoístas<sup>4</sup></p>

      <p class="tall">Muito mais do que crescimento espiritual, a relação com Deguchi proporcionou a Ueshiba a entrada para os 
    círculos da elite política e militar enquanto mestre de artes marciais. Como desfecho dessa admirada exibição, Ueshiba foi 
    capaz de atrair não só apoio financeiro, mas também alunos com capacidades distintas<<sup>5</sup>.</p>

      <p class="tall">A Doutrina espírita, Espiritismo ou Kardecismo, por sua vez, é a doutrina codificada pelo pedagogo francês 
    Hippolyte Léon Denizard Rivail, usando o pseudônimo “Allan Kardec”.</p>

      <p class="tall">O Espiritismo se baseia em cinco obras básicas, escritas por Allan Kardec, através da observação de 
    fenômenos atribuídos a manifestações de inteligências incorpóreas ou imateriais, denominadas espíritos. A codificação espírita 
    está presente nas obras: “O Livro dos Espíritos”, “O Livro dos Médiuns”, “O Evangelho segundo o Espiritismo”, “O Céu e o 
    Inferno” e “A Gênese”.</p>

      <p class="tall">O termo Espiritismo foi cunhado por Kardec em 1857 para definir especificamente o corpo de idéias por ele 
    reunidas e codificadas em “O Livro dos Espíritos”. Refere-se a uma doutrina que trata da “natureza, origem e destino dos 
    espíritos, bem como de suas relações com o mundo corporal e as consequências morais que dela dimanam” (grifo nosso), e 
    fundamenta-se nas manifestações e nos ensinamentos dos espíritos. Também é compreendido como uma doutrina de cunho 
    científico-filosófico-religioso voltada para o aperfeiçoamento moral do homem, que acredita na possibilidade de comunicação 
    com os espíritos através de médiuns. Na publicação do livro O que é o Espiritismo, o codificador a define como “uma ciência 
    que trata da natureza, da origem e da destinação dos espíritos, e das suas relações com o mundo corporal” (grifo nosso)<sup>6</sup>.</p>

      <p class="tall">O espiritismo pode ser visto como uma doutrina estabelecida mediante a fusão da ciência, filosofia e religião, 
    buscando a melhor compreensão não apenas do universo tangível (científico), mas também do universo a esse transcendente 
    (religião).</p>

      <p class="tall">A doutrina espírita, de modo geral, fundamenta-se nos seguintes pontos: Existência e unicidade de Deus, 
    desconstruindo o dogma da Santíssima Trindade (conforme está na primeira questão de “O Livro dos Espiritos” – “Deus é a 
    inteligência suprema, causa primária de todas as coisas”.) (grifo nosso); O universo é criação de Deus, incluindo todos os 
    seres racionais e irracionais, animados e inanimados, materiais e imateriais, que por sua vez, estão destinados a lei de 
    evolução; Existência e imortalidade do espírito, compreendido como individualidade inteligente da Criação Divina (para Kardec, 
    a ligação entre o espírito e o corpo físico, é feita por meio de um conectivo “semimaterial” que denomina de “perispírito”); Volta 
    do espírito à matéria (reencarnação), tantas vezes quanto necessário, como o mecanismo natural para se alcançar a perfeição; 
    Conceito de “criação igualitária” de todos os espíritos, “simples e ignorantes” em sua origem, e destinados invariavelmente 
    à perfeição, com aptidões idênticas para o bem ou para o mal, dado o livre-arbítrio; Possibilidade de comunicação entre os 
    espíritos encarnados (vivos) e os espíritos desencarnados (mortos), por meio da mediunidade; Lei de causa e efeito, 
    compreendida como mecanismo de retribuição ética universal a todos os espíritos, segundo a qual nossa condição é resultado 
    de nossos atos passados; Pluralidade dos mundos habitados, a idéia de que a Terra não é o único planeta com vida inteligente
    no universo; Jesus como sendo o guia e modelo para toda a humanidade.</p>

      <p class="tall">Além disso, pode-se citar como características secundárias: A noção de continuidade da responsabilidade 
    individual por toda a existência do espírito; Progressividade do espírito dentro do processo evolutivo em todos os níveis da 
    natureza; Ausência total de hierarquia sacerdotal; Abnegação na prática do bem, ou seja, não se deve cobrar pela prática da 
    caridade nem o fazer visando segundas intenções, toda a prática espírita é gratuita, como orienta o princípio moral do 
    evangelho: “Dai de graça o que de graça recebestes”; Uso de terminologia e conceitos próprios, como, por 
    exemplo, perispírito, reencarnação, lei da evolução, lei de causa e efeito, mediunidade, centro espírita; Total ausência 
    de exorcismos, fórmulas, palavras sacramentais, horóscopos, cartomancia, pirâmides, cristais, amuletos, talismãs, culto 
    ou oferenda a imagens ou altares, danças, procissões ou atos semelhantes, paramentos, andores, bebidas alcoólicas ou 
    alucinógenas, incenso e fumo, praticas exteriores ou quaisquer sinais materiais; Ausência de rituais institucionalizados, a 
    exemplo de batismo, culto ou cerimônia para oficializar casamento; Incentivo ao respeito para com todas as religiões e
    opiniões<sup>7</sup></p>

      <p class="tall">Mas, qual a relação do Aikido – uma arte marcial – com o Espiritismo – uma religião, na acepção técnica da 
    palavra?</p>

      <p class="tall">Para responder a esta pergunta, é necessário voltar às raízes religiosas e filosóficas do Aikido, a religião 
    “Oomoto”.</p>

      <p class="tall">A religião Oomoto cultua o Grande Deus do Princípio Criador (Oomoto Sume-Oomikami). Deus é uno e a raiz 
    de todas as religiões: não importa o nome que Lhe dêem - Jeová, Alá, Senhor. De acordo com os ensinamentos da Oomoto, 
    Deus é “Eterno Amor” e está sempre presente junto a nós, procurando proteger-nos. Mas para obter sempre a proteção divina, 
    é preciso que nos mantenhamos em permanente comunhão com Ele, em todos os instantes da vida<sup>8</sup>. Noutra definição, "A
    Oomoto é uma organização religiosa criada pelo Deus verdadeiro a fim de proporcionar alegria de viver e energia vital aos 
    homens, e também para construir na Terra um mundo de paz e tranquilidade, sem antagonismos”.<sup>9</sup></p>

      <p class="tall">No ano 25 da Era Meiji, (1892) a Oomoto foi fundada por “NAO DEGUCHI” (1837- 1918), em Ayabe, 
    província de Kyoto, situada quase no centro do território japonês. No dia 3 de fevereiro (ano novo do calendário lunar) do ano 
    em que a fundadora NAO DEGUCHI completava 55 anos de idade, o “DEUS USHITORA” a possuiu inesperadamente, 
    declarando a destruição e reconstrução dos mundos material e espiritual. Dessa maneira ocorreu a fundação da Oomoto<sup>10</sup>.</p>

      <p class="tall">Mais tarde o Deus fez a Fundadora tomar um pincel, e ela escreveu automaticamente a escrita sagrada 
    denominada “Fudesaki”.</p>

      <p class="tall">O FUDESAKI foi escrito pela Fundadora, analfabeta, em kana cursivos (manuscritos) que indicam apenas a 
    pronúncia de cada sílaba. Ao fim de 26 anos essa escrita somou 10.000 volumes, cada qual constituído de 
    20 folhas de papel japonês<sup>11</sup>.</p>

      <p class="tall">O Fudesaki abrange assuntos muito diversos: as origens e os destinos de Deuses e divindades; a relação 
    entre Deus e o homem; os pecados da humanidade; significado do surgimento da Oomoto; profecias e advertências à 
    humanidade; a reforma espiritual e material do mundo; a finalidade da vida humana; a unificação ou reunião do mundo, entre 
    outras.</p>
    
      <p class="tall">O DEUS USHITORA, que possuiu a Fundadora, é um “espírito-parcela” direto do criador de todo o universo 
    ou o único Deus absoluto. Ele é o governador da Terra, e criou o globo terrestre fazendo crescer todo ser vivo. 
    Interessante a transcrever o diálogo entre NAO e Deus:</p>

      <p class="tall" style="text-align: center; font-style: italic;">“NAO, compre para Mim papel e pincel. Eu escreverei com a sua mão (grifo nosso).” – disse Deus, prosseguindo
    minuciosamente: “Um jo (vinte folhas de papel) custa 2 sen; um pincel – 2 sen, e um bastonete de sumi – 2 sen”.</p>

    <p class="tall">Quando NAO comprou esses objetos, de acordo com Sua ordem, eis que ouve uma voz:</p>

    <p class="tall" style="text-align: center; font-style: italic;">“Esfregue o bastonete de sumi!”, “Tome na mão o pincel!”</p>

    <p class="tall">Imediatamente sua mão começou a mover-se sobre o papel. Essas letras eram ilegíveis para ela que não
    havia frequentado nenhuma escola. Todavia, prosseguiu na escrita, entregando-se a Deus.</p>

    <p class="tall">O quarto onde ela escrevia o Fudesaki destinava-se exclusivamente a esse fim. Sempre vestindo roupas 
    limpas após sua purificação, NAO entrava no quarto e sentava-se diante de uma pequena mesa. Pouco tempo depois, um 
    crente olhou para o interior do quarto de NAO, pela fresta dos fusuma<sup>12</sup> fechados, procurando ver os Sumi e pincéis. NAO 
    escrevia o Fudesaki. Então o crente viu que a figura de NAO não era meiga, mas respeitável como a de um Deus varonil.</p>
    
    <p class="tall"> Dizem que esse Deus segurava na mão um pincel, em posição vertical, mergulhava-o inteiro no sumi e
    escrevia ininterruptamente no papel, até secar. Então Ele embebia-o de novo no sumi, e voltava a escrever. Notando que 
    estava sendo observada, NAO o censurou brandamente: <i>“Este Deus é tão sagaz e tão vigoroso que, sentindo-Se perturbado, 
    bastaria tocar o importuno, para que seu corpo fosse atirado a uma distância de dez metros.”</i><sup>13</sup></p>

    <p class="tall"> A respeito do Fudesaki, o renomado calígrafo japonês, Tan’en Ayamura, emitiu a seguinte opinião: “A escrita
    é constituída quase sempre de traços de pincel feitos com idêntica pressão, espessura e rapidez, do primeiro toque até o final. 
    Isto vem a comprovar que a pessoa que escreveu era dotada de inabalável estado mental. O que admira é que não apenas 
    algumas folhas de papel, mas todos os 10.000 cadernos têm a mesmíssima característica. A escrita de letras tão grossas é 
    possível somente a quem possua habilidade acima do comum. Cada letra apresenta-se impregnada de um espírito suave, rico, 
    desembaraçado, lhano e vivo. Nobreza e majestade pairam sobre essa escrita delicada e singela”. (Tan’en Ayamura, data 
    desconhecida).<sup>14</sup></p>

    <p class="tall"> Alguns parágrafos do Fudesaki merecem transcrição:</p>
    <p class="tall" style="text-align:right; font-style: italic;"> “No velho Reino Divino tudo corria em ordem e paz. Mais tarde, porém, a partir do momento em que os Diabos
    conquistaram a hegemonia, o mundo degenerou mais e mais.” 

    “Veja o mundo que chegou a uma situação sem saída...; é o fim do mundo de animais. O modo arbitrário e egoísta de 
    administrar transformou o mundo num matadouro, onde os mais fortes espoliam os mais fracos. Abandonado neste 
    estado, o mundo perecerá. Para que o mundo não se torne em morada de monstruosas bestas humanas, deverá 
    transformar-se em um novo mundo, por meio de rigoroso acrisolamento e reconstrução” e para isso “já é chegado o 
    Tempo.” 

    “Mas se a reconstrução do mundo se der num instante, a Terra se transformará inevitavelmente num mar de lama e nem 
    trinta por cento da população lograrão viver... O que, todavia, de modo algum acontecerá, pois Eu diligenciarei por reservar 
    algum tempo para que a reconstrução do mundo decorra da reforma dos corações humanos.” </p>

    <p class="tall"> Pois bem, continuando nossa análise do cunho religioso do Aikido e relacionando os fundamentos da religião
    Oomoto com os ensinamentos daquela arte marcial, podemos afirmar que “O Sensei”<sup>15</sup> buscou - com sucesso, diga-se de 
    passagem – incutir em seu sistema marcial os mais aprofundados sentimentos de bondade, generosidade, não-agressão e,
    principalmente, de caridade.</p>

    <p class="tall"> Vejamos então algumas frases ditas por “O Sensei” em várias passagens: </p>

    <p class="tall" style="text-align: center; font-style: italic;"> "O Aikido não é uma técnica para lutar contra um inimigo ou derrotá-lo. É uma maneira de conciliar as diferenças que
    existem no mundo e fazer dos seres humanos uma família. Significa que o segredo do Aikido é a busca da harmonia com 
    o Universo, é tornar-nos unos com o Universo. Seus praticantes devem buscar esse entendimento por meio de 
    treinamento diário." 
    <br><br>
    "Eu não estou ensinando técnicas marciais, estou ensinando a não-violência." 
    <br><br>
    "Aiki significa a união entre o corpo e o espírito e é a manifestação desta verdade. Mais do que isso, Aiki nos permite 
    harmonizar céu, terra e a humanidade como uma só." 
    <br><br>
    "O verdadeiro pacifista é aquele capaz de causar danos imensuráveis mas escolhe não fazê-lo quando provocado." 
    <br><br>
    “Mentes para servir à paz de todos os seres humanos no mundo são necessárias ao Aikido. Não as mentes daqueles que 
    desejam ser fortes ou que praticam somente para derrotar um oponente.” 
    <br><br>
    “Se o coração é impuro, estarás cheio de tensão interior, de orgulho, de desordem, de confusão, de mil enfermidades 
    físicas, mentais e emocionais. Jamais poderás compreender o Aiki se seu coração não se purifica. Deves, pois, limpá-lo 
    para ter paz consigo e com o mundo, não sendo inimigo de nada e não vendo nada como seu inimigo.” 
    <br><br>
    “Eu nunca sou derrotado, por mais rápido que o inimigo possa atacar. Não é porque minha técnica é mais rápida do que a 
    do inimigo. Não é uma questão de velocidade. A luta é finalizada antes mesmo de já ter começado.” 
    <br><br>
    “Quando o inimigo tenta lutar contra mim, o próprio Universo, ele precisa quebrar a harmonia do Universo. Por isso, no 
    momento em que sua mente está focada em lutar comigo, ele já está derrotado. Não existe nenhuma medida de tempo - 
    rápido ou devagar.” 
    <br><br>
    “Aqueles que tem uma mente conturbada, uma mente de discórdia, já foram derrotados desde o começo.” 
    <br><br>
    “Então, como você pode endireitar uma mente conturbada, purificar seu coração, e ser harmônico com as atividades de 
    todas as coisas da Natureza? Você deveria primeiro fazer do coração de Deus o seu coração. É um Grande Amor, 
    Onipresente em todos os cantos e em todos os tempos do Universo. 'Não há desacordo no Amor. Não há inimigos do Amor.'
    Uma mente conturbada (em desacordo), pensando na existência do inimigo, não é mais consistente com o desejo
    de Deus.”
    <br><br>
    “Aqueles que não concordam com isso não podem estar em harmonia com o Universo. O budo deles é de destruição. Não 
    é um budo construtivo.” 
    <br><br>
    “Portanto, competir nas técnicas, ganhar ou perder, não é o verdadeiro budo. Verdadeiro budo não conhece derrota. 
    'Nunca derrotado' significa 'nunca lutando.'" 
    <br><br>
    “O Verdadeiro budo é um trabalho de Amor. É um trabalho de dar Vida para todos os seres, e não de matar e lutar uns 
    com os outros. Amor é a divindade guardiã de tudo. Nada pode existir sem Amor. Aikido é a realização do Amor.” 
    <br><br>
    “Nós incessantemente rezamos para que as lutas não aconteçam. Por esta razão, não há torneios no Aikido. O espírito 
    do Aikido é de um ataque amoroso e de uma reconciliação pacífica. Neste foco, nós juntamos e unimos os oponentes com 
    a intenção poderosa do Amor. Através do Amor, nós somos capazes de purificar os outros.” 
    <br><br>
    “Compreenda o Aikido primeiramente como budo e então como um meio de serviço para construir a Família 
    Mundial. Aikido não é para um único país ou alguém em particular. Seu único propósito é realizar o trabalho de Deus.” 
    <br><br>
    “Verdadeiro budo é a proteção amorosa de todos os seres com um espírito de reconciliação. Reconciliação significa 
    permitir a realização da missão de todos.” 
    <br><br>
    “O "Caminho" significa ser Uno com o Desejo de Deus e praticá-lo. Se estamos só um pouquinho fora dele, não é mais o 
    Caminho.” 
    <br><br>
    “Nós podemos dizer que Aikido é um caminho para varrer os demônios com a sinceridade da nossa respiração ao invés da 
    espada. Isto é, mudar a mente demoníaca do mundo para o Mundo do Espírito. Esta é a missão do Aikido. A mente 
    demoníaca sucumbirá na derrota e o Espírito se erguerá na vitória. Então o Aikido colherá frutos neste mundo.”</p>

    <p class="tall"> A caridade nos ensinamentos de “O Sensei” é tão visível que ele demonstra insatisfação enquanto todas as 
    pessoas não compreenderem seu <i>Budo</i>!</p>

    <p class="tall">A caridade salta aos olhos na execução das técnicas durante os treinos, onde o nage<sup>16</sup> tem cuidado para não 
    ferir o uke<sup>17</sup>. A caridade pode, ainda, ser vista nos seminários<sup>18</sup>, consubstanciada nas palavras dos Sensei que os ministram, 
    nos passando lições de vida, de amor, de compreensão das agressões e de como nos harmonizarmos com nossos atacantes, 
    redirecionando seu ódio.</p>
    
    <p class="tall">Por certo, feitas as considerações acima, podemos traçar o vínculo existente entre o Aikido e o Espiritismo. 
    O primeiro aspecto de relação que podemos visualizar é aquele que se refere ao princípio da existência de 
    uma “alma” ou “espírito”, que seria imortal e suplantaria nossa existência terrena, sobrevivendo ao perecimento da ‘carne’.</p>
    
    <p class="tall">Em várias passagens e palavras de “O Sensei” podemos verificar menções que nos remetem à crença na 
    imortalidade da alma e numa espécie de espírito imortal, o cerne do homem, senão vejamos.</p>
  
    <p class="tall">De uma das definições de Aikido – “caminho do espírito harmonioso” – notamos a presença da palavra
    “espírito”, verbete comum ao espiritismo, que crê na imortalidade da alma. Mencionamos antes que na Religião Oomoto o
    “DEUS USHITORA” a possuiu (NÃO DEGUCHI) inesperadamente, declarando a destruição e reconstrução dos mundos
    material e espiritual. Aqui, mais uma vez, vemos uma menção a um “mundo espiritual”, e isso anterior à criação do Aikido por 
    Morihei Ueshiba”. Não bastasse, o “Deus Ushitora”, como vimos, seria um “espírito-parcela” do Criador, remetendo à idéia de 
    algo que transcende a vida carnal.</p>

    <p class="tall">Agora, façamos uma análise minuciosa de algumas palavras do fundador (Morihei Ueshiba). 
    O Sensei disse que <i>“... o segredo do Aikido é a busca da harmonia com o Universo, é tornar-nos unos com o 
    Universo”</i>, o que nos remete à idéia do “espírito-parcela”. Ele também disse que <i>"Aiki significa a união entre o corpo e o 
    espírito...”</i>, mais uma vez incutindo a idéia de um ser interior, que transcende a carne.</p>

    <p class="tall">Nas próprias doutrinas Samurai<sup>19</sup> ressaltam aos olhos as crenças sobre a imortalidade da alma. O nome 
    “samurai” significa, em japonês, “aquele que serve”. Portanto, sua maior função era servir, com total lealdade e empenho, o 
    Imperador. A morte, para o samurai, era um meio de perpetuar a sua existência. Tal filosofia aumentava a eficiência e a não-
    hesitação em campos de batalha, o que veio a tornar o samurai, segundo alguns estudiosos, o mais letal de todos 
    os guerreiros da antiguidade. O caminho espiritual também fazia parte do ideal de homem perfeito que esses guerreiros 
    buscavam. Nessa busca os samurais descobriram o Budismo<sup>20</sup>, com influências do Zen<sup>21</sup>, como um caminho que conduzia à
    calma e à harmonia<sup>22</sup>.</p>

    <p class="tall">Ora, os ensinamentos de O Sensei não se assemelham em muito com aqueles pregados pelo Cristo de que 
    “não existe salvação fora da caridade”? De que a harmonia entre os seres é a chave para a paz? De que a não-violência é o 
    movimento natural do universo e não o contrário?</p>

    <p class="tall">Outrossim, o Espiritismo acredita nas idéias de possessão<sup>23</sup> (aqui entendida não no sentido hoolyoodiano de 
    entrada de demônios em corpos de pessoas, mas de tomada dos pensamentos e movimentos por espíritos perturbados), 
    reencarnação<sup>24</sup> (diferentemente da idéia de ressurreição pregada pelo Cristianismo) e de psicografia<sup>25</sup>.</p>

    <p class="tall">Em relação à possessão, com o Espiritismo, através da observação, repetição dos fatos e comprovação dos 
    fenômenos, concluiu-se experimentalmente que é possível comunicar-se com aqueles que já estão no mundo espiritual, isto é, 
    cujos corpos físicos já morreram. Assim, descobriu-se que nas sessões de exorcismo não era o diabo que se comunicava, mas 
    sim, espíritos perturbados que mais não eram do que as almas de pessoas que viveram na Terra e que continuavam em estado 
    de perturbação. Nesse sentido, numa sessão espírita não se expulsa o <i>“diabo”</i> (já que este não existe), mas doutrinam-
    se aquelas pessoas que por desconhecimento da vida espiritual andam em perturbação nesse mesmo plano.</p>

    <p class="tall">A reencarnação obedece a um princípio de identidade de freqüências, ou seja, o espírito reencarna em um 
    determinado continente, em um determinado país, em uma determinada região desse país, em uma determinada localidade 
    dessa região, com determinadas características culturais (idioma, usos, costumes, valores, tradições, história etc.), bem como 
    em uma determinada família, de acordo com a sintonia que a freqüência do seu pensamento consiga estabelecer em relação a 
    cada um desses elementos.</p>

    <p class="tall">O espírito realiza a reencarnação conscientemente, inclusive traçando o seu próprio plano geral para a 
    existência material que está se iniciando. O espírito reencarnante, de acordo com suas limitações, será mais ou menos 
    auxiliado por espíritos com mais conhecimento e com os quais tenha afinidade. No entanto, se não estiver suficientemente 
    equilibrado ou consciente, será orientado no planejamento de sua passagem pelo polissistema material.</p>

    <p class="tall">Todavia, reencarnado o espírito, inicia-se o processo de existência corporal no polissistema material. É um 
    processo aberto, pois a trajetória pessoal do encarnado segue o exercício do seu livre-arbítrio. Portanto, não há que se falar 
    em destino, em caminhos previamente traçados.</p>

    <p class="tall">O espírito encarnado, fundamentando-se em seu existente (a bagagem de conhecimentos e experiências 
    adquiridos ao longo de toda a sua história, seja encarnado, seja desencarnado), passa a exercitar sua capacidade, a constatar 
    e desenvolver suas potencialidades, enfim, passa a construir seu momento presente e seu momento futuro. Vai enfrentando 
    contradições, dificuldades, obstáculos, facilidades, administrando encontros e desencontros, permanecendo no seu plano geral 
    ou se desviando em função de algumas variáveis do processo, mas sempre de acordo com sua vontade.</p>

    <p class="tall">No exercício do livre-arbítrio, o espírito encarnado vai construindo seu equilíbrio ou seu desequilíbrio, de 
    acordo com a maneira pela qual enfrenta as situações e a vida. Vai, por assim dizer, determinando-se, segundo a natureza de 
    seus pensamentos e atos. Por menos que faça, ou por mais que se desequilibre, o espírito sempre alcança progressos em um 
    ou outro aspecto do seu ser.</p>

    <p class="tall">A evolução não está necessariamente vinculada ao tempo de vida material, mas à intensidade com que ela é 
    vivida. A quantidade de experiências e o aproveitamento que é feito delas é fundamental para o crescimento do espírito, não 
    importando se as experiências estão sendo vivenciadas nos polissistemas material ou espiritual.</p>

    <p class="tall">É de se ressaltar que, entre uma encarnação e outra, o espírito continua trabalhando, continua aprendendo, 
    continua evoluindo, de modo que ele não reencarna no mesmo estágio em que desencarnou.</p>

    <p class="tall">A Doutrina Espírita trabalha, atualmente, com a hipótese de que o processo reencarnatório envolve os
    conceitos de missão, provação, expiação e carma.</p>

    <p class="tall">Vale ressaltar que no entendimento atual da Doutrina, os processos reeencarnatórios apresentam facetas 
    desses quatro conceitos, mas que algumas reencarnações podem apresentar o predomínio de algumas dessas características.
    Eles não são conseqüência de uma interferência ou controle externo ao espírito reencarnante, descartando-se, portanto 
    qualquer idéia de castigo, punição ou recompensa. Eles são decorrentes da lei de causa e efeito e das condições de equilíbrio 
    e harmonia do espírito.</p>

    <p class="tall">Missão é a situação na qual o espírito reencarnante aplica conhecimentos internalizados a favor de uma 
    pessoa ou do grupo de sua convivência.</p>

    <p class="tall">Provação é a situação na qual o conhecimento em processo de acomodação e internalização deve ser 
    vivenciado; é a situação na qual o espírito é desafiado ao limite de seu conhecimento.</p>

    <p class="tall">Expiação não se refere à aplicação de conhecimento, mas, sim, a uma conseqüência de um conhecimento 
    aplicado, que provocou conseqüências difíceis, desagradáveis, muitas vezes dolorosas, que o seu responsável deverá 
    enfrentar.</p>

    <p class="tall">Carma ainda é um conceito útil dentro da concepção da Doutrina, desde que se esteja atento para o seu 
    significado, diverso do de outras Doutrinas. Para o Espiritismo, carma caracteriza a situação na qual o espírito está 
    enfrentando as conseqüências de atos seus que lhe provocaram um desequilíbrio muito intenso, tanto em qualidade como em 
    quantidade, e que, pela sua intensidade, o espírito poderá levar toda uma encarnação, ou mais de uma, para recuperar seu.
    equilíbrio<sup>26</sup>.</p>

    <p class="tall">Os médiuns psicógrafos podem ser “Mecânicos”, “Intuitivos”, “Semi-mecânicos” e os “Inspirados”.</p>

    <p class="tall">Os médiuns mecânicos se caracterizam pelo fato de movimentar as mãos escrevendo sob a influência direta 
    dos Espíritos, sem interferência da própria vontade. Agem como máquinas a transmitir do invisível para o mundo material. São 
    raros.</p>

    <p class="tall">Quando o Espírito age diretamente sobre a mão, dá-lhe uma impulsão completamente independente da 
    vontade do médium. Ela avança sem interrupção e contra a vontade do médium, enquanto o Espírito tiver alguma coisa a 
    dizer, e pára quando ele o disser.</p>

    <p class="tall">O que caracteriza o fenômeno, nesta circunstância, é que o médium não tem a menor consciência do que
    escreve. A inconsciência absoluta, nesse caso, caracteriza os que chamamos de médiuns passivos ou mecânicos. Esta 
    faculdade é tanto mais valiosa quanto não pode deixar a menor dúvida sobre a independência do pensamento daquele que 
    escreve.</p>

    <p class="tall">Os médiuns intuitivos recebem as mensagens dos Espíritos desencarnados por meio da sintonia psíquica
    direta entre sua mente e a do comunicante. Eles precisam compreender o pensamento sugerido, assimilá-lo, para depois 
    transmiti-lo revestido com suas próprias idéias. São muito comuns.</p>

    <p class="tall">O médium intuitivo age como um intérprete. Para transmitir o pensamento ele precisa compreendê-lo, de 
    certa maneira assimilá-lo, a fim de traduzi-lo fielmente. Esse pensamento, portanto, não é dele: nada mais faz do que passar 
    através do seu cérebro. É exatamente esse o papel do médium intuitivo.</p>

    <p class="tall">Os médiuns semi-mecânicos são aqueles que sentem a mão ser movimentada, mas ao mesmo tempo têm 
    consciência do que escrevem. No primeiro caso, o pensamento vem após a escrita; no segundo, antes da escrita, e no terceiro, 
    junto com ela. Os médiuns semi-mecânicos são os mais numerosos.</p>

    <p class="tall">A última variedade de médiuns é a dos inspirados. Esse tipo de médium é uma variação dos médiuns 
    intuitivos, com a diferença de que nos inspirados é muito mais difícil distinguirmos o pensamento do Espírito, daquele que é do 
    médium. A mediunidade inspirada é proveniente da mediunidade generalizada ou natural, que todas as pessoas possuem em 
    maior ou menor grau.</p>

    <p class="tall">Todos os que recebem, no seu estado normal ou de êxtase, comunicações mentais estranhas às suas idéias, 
    sem serem, como estas, preconcebidas, podem ser considerados médiuns inspirados. Trata-se de um variedade intuitiva, com 
    a diferença de que a intervenção de uma potência oculta é bem menos sensível sendo mais difícil de distinguir no inspirado o 
    pensamento próprio do que foi sugerido. O que caracteriza este último é, sobretudo, a espontaneidade.</p>

    <p class="tall">Recebemos a inspiração dos Espíritos que nos influenciam para o bem ou para o mal. Mas ela é 
    principalmente a ajuda dos que desejam o nosso bem, e cujos conselhos rejeitamos com muita freqüência. Aplica-se a todas 
    as circunstâncias da vida, nas resoluções que devemos tomar<sup>27</sup>.</p>

    <p class="tall">Do ponto de vista do Espiritismo, a história está repleta de fenômenos espirituais e manifestações de 
    possessões, reencarnação e psicografia, como por exemplo: a entrega dos dez mandamentos por Deus a Moisés; a 
    “ressurreição” de Lázaro e do próprio Jesus Cristo; a descoberta, por Jesus, da futura traição de Judas Escariotes; as 
    aparições de Maria, mãe de Jesus, dentre tantas outras.</p>

    <p class="tall">Como um dos fundamentos da Religião Oomoto é o de que todas as religiões tem uma raiz comum, um Deus 
    único, nos sentimos a vontade para afirmar, sem qualquer medo ofender os seguidores deste tipo de culto, que o que NAO 
    DEGUCHI experimentou quando do episódio com o Deus USHITORA, nada mais foi do que um caso de possessão e psicografia.</p>

    <p class="tall">Mas, qual a relação disso com O Sensei?</p>

    <p class="tall">Em primeiro lugar, O Sensei era seguidor da Religião Oomoto, que como vimos, possui muitos pontos em 
    comum com o Espiritismo.</p>

    <p class="tall">Em várias passagens do livro “Aikido Shugyo – Harmonia no Confronto”<sup>28</sup>, escrito por Gozo Shioda<sup>29</sup>
    encontramos diversas passagens narradas pelo mesmo sobre aparentes “poderes mágicos” que O Sensei teria.</p>

    <p class="tall">Vários são os episódios narrados em que Morihei Ueshiba tinha premonições sobre a chegada de visitas em 
    seus Dojos. Também é contado no livro citado, um episódio em que O Sensei desafia vários grandes atiradores japoneses da 
    época, escapando ileso dos tiros. E mais, em certa ocasião, quando de uma viagem de trem para Tanabe, Gozo Shioda teria 
    tentado testar estes poderes atacando Morihei enquanto ele dormia, tendo tido seu braço (de Gozo Shioda) segurado antes 
    mesmo de iniciar o ataque, quando então O Sensei teria lhe dito o seguinte: “sabe por que ninguém consegue me acertar? 
    Porque os espíritos falam comigo. Os espíritos me avisam”.</p>


    <p class="tall">E por tudo isso, independentemente e com respeito à crença religiosa de cada um, nos sentimos seguros 
    para concluir que:</p>

    <p class="tall">1) Para que se compreenda a fundo a arte marcial do Aikido, é necessário que se estude com afinco as questões religiosas 
    inerentes às suas raízes, sendo ele (o Aikido) indissociável da idéia de religião; 
    2) Morihei Ueshiba era um espírito superior e iluminado, que tinha uma missão nobre nesta existência terrena, a de levar amor 
    e paz aos corações humanos através de um Budo de vida reta e caridosa; 
    3) A prática do Aikido em nossos Dojos deve ter como foco principal o caminho de retidão pregado por Morihei Ueshiba, sendo 
    que a técnica – compreendido o objetivo principal – aparecerá naturalmente; 
    4) O Aikido, assim como nos pregou O Sensei, é sim, um dos caminhos que nos levam a Deus.</p>

    <p class="tall">Por fim, cumpre-nos lembrar sobre as várias histórias que nos foram contadas pelo saudoso Reishin Kawai<sup>30</sup>
    sobre sua vida, o que de certa forma reforçam as conclusões aqui chegadas. Por motivos óbvios não poderemos nos 
    aprofundar neste artigo sobre a pessoa de Kawai Shiran, uma vez que, além de não ser o objeto do presente artigo, não nos 
    sentimos seguros ainda para escrever sobre o mesmo.</p>


