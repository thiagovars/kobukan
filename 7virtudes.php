              <h3><strong>Artigo:</strong> As 7 virtudes do samurai na prática do aikido</h3>
              <p class="drop-caps">Um dos motivos que diferenciam o Aikido das demais artes marciais é o fato de
              manter as tradições de etiqueta da antiga cultura marcial. O Aikido enquanto Budô, e 
              não luta esportiva, busca algo mais para os praticantes do que uma medalha ou troféu. 
              Esta busca pela evolução dos aikidoístas é pautada pelo antigo código de conduta dos 
              Samurais, também conhecido como Bushido.</p>

              <p class="tall">Um dos preceitos salientados no Bushido, refere-se às virtudes dos samurais,
              em número de sete. Estas sete virtudes são trazidas, enquanto elemento simbólico, às 
              práticas do aikido por meio da representação das sete pregas do Hakama, adotado como 
              indumentária dos graduados.</p>

              <p class="tall">Abaixo discorro sobre cada uma destas virtudes, e sua correspondência com a
              prática do Aikido nos tempos atuais:</p>

              <p class="tall">1a GI: pode ser entendida como <strong>dever</strong>, obrigação, mas também retidão, integridade,
              justiça e moralidade, como os termos abaixo derivados:</p>
              <ul>
                <li><strong>Gimu:</strong> dever, obrigação;</li>
                <li><strong>Giri:</strong> Sentimento de dívia, gratidão;</li>
                <li><strong>Giri gatai hito:</strong> Pessoa cumpridora dos deveres sociais;</li>
                <li><strong>Seigui:</strong> Justiça, Caminho da Verdade. (1)</li>
              </ul>

              <p class="tall">2a YUU: traduzida como coragem, pode também ser entendida como bravura. Neste
              sentido vemos que no desenvolvimento do Aikido, o postulante deve superar a si 
              mesmo e as suas próprias fraquezas. Se recuar diante dos desafios que se põem à sua 
              frente, muitas vezes abandona o caminho por medo de persistir. A coragem é assim 
              representada pela conduta de persistir, apesar do medo, dos receios e de suas próprias 
              fragilidades. Emblemáticos são os exames de faixa onde os deshis são testados não 
              apenas física, mas psicologicamente, expondo-se à apreciação de uma banca avaliadora. 
              Sem bravura ou coragem, não seria possível trilhar o caminho do desenvolvimento no 
              Aikido.</p>
              <p class="tall">3a. JIN: benevolência. A benevolência ou compaixão para um genuíno praticante de
              Aikido é uma virtude essencial e se manifesta pela capacidade de colocar-se, de forma 
              generosa, no lugar dos seus companheiros, por meio do sentimento de auxílio à sua 
              evolução. Isso é representado pela atitude frente ao seu “oponente”, ou melhor, dizendo, 
              companheiro de técnica (seja atuando como uke ou nague), com vistas a evitar qualquer 
              forma de dano a outrem. O verdadeiro aikidoísta possui o espírito do altruísmo: prefere 
              machucar a si mesmo do que ao colega. Além disso, de forma benevolente busca 
              contribuir para o desenvolvimento dos menos graduados ou dos colegas com maiores 
              dificuldades.</p>

              <p class="tall">4a REI: respeito, cortesia, polidez. Aplicada à conduta do aikidoísta denota a reverência
              que este possui com todos os elementos envolvidos na sua prática. Ao cumprimentar, 
              de forma reverente e cortez, o altar (kamiza), bem como seu Sensei e cada um de seus 
              colegas, sejam eles mais ou menos graduados, o aikidoísta demonstra seu respeito por 
              todos e tudo o que permite o seu próprio aprimoramento. O REI pontua o início e o fim 
              de cada trabalho ou técnica, por meio do cumprimento. Toda a “etiqueta” do Aikido, 
              com vistas ao respeito à prática e aos objetos de trabalho, também fazem parte deste 
              conceito.</p>

              <p class="tall">5a. MAKOTO: sinceridade. A intenção e aplicação dos valores e virtudes deve ocorrer 
              por meio de relações galgadas na verdade de coração. Nesse sentido, aplicado ao aikido, 
              vejo a verdade do praticante em, de forma sincera, querer sua evolução, dos demais 
              companheiros e do grupo. O termo verdade também pode ser obtido pela combinação 
              das palavras japonesas guen (palavra) e jo (perfeito). Nessa óptica, a palavra do 
              aikidoísta, também deve ser mantida, a fim de manter compromissos assumidos com o 
              grupo e consigo mesmo. Como afirmou Inazos Nitobe: “Sem veracidade e sinceridade 
              a polidez é uma farsa e uma representação” (2).</p>

              <p class="tall">6a. MEIYO: honra. Podemos aplicar essa virtude, de forma diferente do orgulho, à
              valorização de si e do grupo e linhagem a qual está inserido. Em detrimento de qualquer 
              caráter comparativo com os demais grupos ou artes, o aikidoísta valoriza seu grupo 
              e hierarquia pela consciência do valor e da importância dessa estrutura para o seu 
              caminho (do). Como exemplo, cito a honra que sentimos por fazer parte do grupo do 
              maior mestre que o Brasil já teve, dedicando sua vida e seus esforços para que o aikidô 
              brasileiro se encontre no atual patamar, Kawai shihan.</p>

              <p class="tall">7a. CHU: lealdade. Tanto o budismo quanto os preceitos confuncianos tornaram a
              obediência aos pais como a primeira dívida do ser humano. “O bushido afirma que 
              o interesse da família e dos seus membros é intacta, una e inseparável.”(3). O ápice 
              desse leadade era anteriormente representado pelo seppuku (harakiri). Na prática do 
              aikido, entendo esse conceito como o dever de fidelidade e lealdade ao grupo, sendo 
              leal aos seus superiores e a própria preservação da técnica, evitando sua distorção ou 
              disseminação indevida. Como recomendava Ô Sensei, <strong>“não ensinar a arte à perversos, 
              que possam fazer da arte motivo de destruição”</strong>.</p>

              <p class="tall">Vejo assim que, embora sendo preceitos de séculos passados, as virtudes dos
              samurais ainda se mantém preservadas na prática do Aikido dos dias atuais. É também 
              por isso que diferenciamos nossa Arte Marcial de tantas outras, e devemos nos esforçar 
              para manter viva essa tradição.</p>


